package Basics;

public class NumericOperations {
    public static void main(String[] args) {
        System.out.println( 10/3 );             //a
        System.out.println( (double)(10/3) );     //b
        System.out.println( 10/3.0 );           //c
        System.out.println( 10 + 7.2 );         //d
        System.out.println( (int)(10+7.2) );            //e
        System.out.println( 1-2/3);        //f
        System.out.println( 1-2.0/3.0 );
        System.out.println( 1.0/3.0 );
        System.out.println( (double) (2+3) );
        System.out.println( 10+(int)(7.2) );
    }
}
