package Basics;

public class CharacterIteration {
    public static void main(String[] args) {
        for( char c='a'; c<='z'; c++){
            System.out.println("Index:"+(byte)c+" Karakter:"+c);
        }
        for( char c='A'; c<='Z'; c++) {
            System.out.println("Index:" + (byte) c + " Karakter:" + c);
        }

    }
}
